my_dict = {
     "name": "Alice",
     "age": 25,
     "city": "New York"
}

#Access a value using a key.
print(my_dict["name"])

#Add a new key-value pair to the dictionary.
my_dict["profession"] = "Engineer"

#Update the value of an existing key.
my_dict["age"] = "26"

#Delete a key-value pair from the dictionary.
del my_dict["city"]